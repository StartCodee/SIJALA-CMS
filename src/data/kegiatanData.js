/* ── Indonesian date helpers ── */
const MONTH_IDX = {
  januari: 0, februari: 1, maret: 2, april: 3, mei: 4, juni: 5,
  juli: 6, agustus: 7, september: 8, oktober: 9, november: 10, desember: 11,
};

/** Parse "21 Januari 2026" → JS Date */
export function parseDateId(d) {
  const [day, month, year] = d.split(" ");
  return new Date(+year, MONTH_IDX[month.toLowerCase()] ?? 0, +day);
}

/** Look up a single kegiatan by slug. */
export function getKegiatanBySlug(slug) {
  return kegiatanList.find((k) => k.slug === slug) ?? null;
}

/** Look up a single kegiatan by id. */
export function getKegiatanById(id) {
  return kegiatanList.find((k) => k.id === Number(id)) ?? null;
}

/**
 * Return a Set of "YYYY-M-D" strings representing dates that have events.
 * Used by the calendar to show blue dots.
 */
export function getEventDateKeys() {
  const keys = new Set();
  for (const k of kegiatanList) {
    const d = parseDateId(k.date);
    keys.add(`${d.getFullYear()}-${d.getMonth()}-${d.getDate()}`);
  }
  return keys;
}

/**
 * Get events for a specific date.
 * @param {Date} date
 */
export function getKegiatanByDate(date) {
  const key = `${date.getFullYear()}-${date.getMonth()}-${date.getDate()}`;
  return kegiatanList.filter((k) => {
    const d = parseDateId(k.date);
    return `${d.getFullYear()}-${d.getMonth()}-${d.getDate()}` === key;
  });
}

/**
 * Get upcoming events from a given date (sorted asc).
 * @param {Date} fromDate
 * @param {number} limit
 */
export function getUpcomingKegiatan(fromDate, limit = 5) {
  const ts = fromDate.getTime();
  return kegiatanList
    .filter((k) => parseDateId(k.date).getTime() >= ts)
    .sort((a, b) => parseDateId(a.date) - parseDateId(b.date))
    .slice(0, limit);
}
